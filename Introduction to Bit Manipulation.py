#Implement Bitwise

num1 = 10
num2 = 4

print("num1 and num2 are:", num1, num2)
print("num1 | num2 are:", num1 | num2)  #OR
print("num1 ^ num2 are:", num1^num2)    #XOR
print("num1 << num2 are:", num1 << num2)    # left bit shifting
print("num1 >> num2 are:", num1>>num2)         # right bit shifting
print("~num1 : ",~num1)      #not
print("~num2 : ",~num2)